REMOTE=$(git rev-parse "$UPSTREAM")
BASE=$(git merge-base @ "$UPSTREAM")

if [ $LOCAL = $REMOTE ]; then
    echo "SIN ACTUALIZACIONES"
elif [ $LOCAL = $BASE ]; then
git pull && 
exit 1
